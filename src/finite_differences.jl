function fdcoeffs(derivative_order::Int, accuracy_order::Int)::Vector{Float64}

    if derivative_order == 1


        stencil = get!(precomputed_coeffs_first, accuracy_order) do
            error("order $accuracy_order not supported")
            compute_coeffs_first(accuracy_order)
        end


    elseif derivative_order == 2
        stencil = get!(precomputed_coeffs_second, accuracy_order) do
            error("order $accuracy_order not supported")
            compute_coeffs_second(accuracy_order)
        end

    else
        throw(ArgumentError("derivative order $derivative_order not supported"))
    end

    return stencil

end

# private functions
function compute_coeffs_first(k::Int)
    x = 1:k                             # 1:k range
    powers = collect(1:2:2k)            # odd powers: 1,3,5,...

    # Build matrix A (k×k)
    A = [2 * xi^p for p in powers, xi in x]

    b = zeros(k)
    b[1] = 1.0

    cd = A \ b                          # solve linear system
    return cd
end

